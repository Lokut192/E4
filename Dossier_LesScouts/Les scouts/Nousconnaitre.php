                Par la loi de Finances, votée le 21 décembre 2017, l’Impôt de Solidarité sur la Fortune (ISF) est remplacé en 2018 par l’Impôt sur la Fortune Immobilière (IFI), qui porte uniquement sur le patrimoine immobilier.<br><br>

                La réduction d’impôt au titre des dons est maintenue dans le cadre de l’IFI : si vous êtes redevable de l’IFI, vos dons sont déductibles à 75% dans la limite d’un plafond de 50 000 euros. En revanche, les investissements dans les PME ne sont plus déductibles de l’IFI.<br><br>

                Régime dérogatoire pour cette année : si vous avez réalisé des dons ou investissements au capital de PME éligibles à la réduction d’ISF entre la date limite de déclaration de l’ISF 2017 et le 31 décembre 2017, la réduction sera imputable sur l’IFI 2018 dans les mêmes conditions et limites que pour l’ISF 2017.<br><br>

                Les fonds versés à cette fondation sont exclusivement destinés à financer les activités de notre Mouvement. En décidant aujourd’hui d’adresser un don à la Fondation des EEUdF, vous nous donnez les moyens de faire vivre à toujours plus de jeunes la grande aventure du scoutisme unioniste.
