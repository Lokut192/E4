            <h1 id="histoire">Histoire</h1>
            <br>
            <br>
            <br>
            <br>
            <p>
                Fondé en 1907 par Lord Robert Baden-Powell, le scoutisme est aujourd’hui le plus grand mouvement de jeunesse mondial présent sur les 5 continents,
                regroupant plus de 38 millions de jeunes à travers le monde. Les Éclaireuses et Éclaireurs Unionistes de France ont vu le jour en 1911 et sont aujourd’hui
                membres de la Fédération du Scoutisme Français et de l’Organisation Mondiale du Mouvement Scout (OMMS) et de l’Association Mondiale des Guides et Éclaireuses (AMGE).<br><br>

                2018 Congrès national des Éclaireuses et Éclaireurs Unionistes de France à Miramont-de-Guyenne (Lot-et-Garonne)<br><br>

                2014 Congrès national des Éclaireuses et Éclaireurs Unionistes de France à Lusignan (Vienne)<br><br>

                2011 Centenaire des Éclaireuses et Éclaireurs Unionistes de France<br><br>

                2007  Centenaire du scoutisme mondial, 21e jamboree en Angleterre : «  Un monde, une promesse »<br><br>

                1999 Camp national « Les Clés de l’Avenir » (LCA), 2 600 jeunes rassemblé·e·s à Tonneins (Lot-et-Garonne)<br><br>

                1990 En juillet, 32e Conférence Mondiale de l’Organisation Mondiale du Mouvement Scout (OMMS), à Paris<br><br>

                1988 Le 5 juin, le Programme des Nations unies pour l’Environnement récompense le Mouvement Scout pour son travail exemplaire dans le domaine de la protection de la nature<br><br>

                1982 Camp national « Pleins Feux sur l’Aventure » (PFA), 3000 éclaireuses et éclaireurs unionistes réuni·e·s au Chambon-sur-Lignon (Haute-Loire)<br><br>

                1970 Le 18 janvier à l’Assemblée générale de Dijon, fusion des Éclaireurs Unionistes et de la Fédération des Française des Éclaireuses, naissance de la Fédération Française des Éclaireuses et Éclaireurs Unionistes de France<br><br>

                1957 Création des Tisons, association rassemblant d’ancien·ne·s éclaireuses et éclaireurs unionistes<br><br>

                1951 Camp national de Wallabach (Alsace) pour le 40e anniversaire des Éclaireurs Unionistes de France<br><br>

                1947 « Jamboree Mondial de la Paix », à Moisson (Yvelines)<br><br>

                1942-44 Actions de résistance des Éclaireurs Unionistes et des Éclaireurs Israélites (EI) du maquis de Vabre (Compagnie Marc Huguenau) dans le Tarn<br><br>

                1940 Fin septembre, charte de l’Oradou et fondation de la Fédération du Scoutisme Français. En zone occupée, les activités scoutes sont interdites mais se poursuivent clandestinement<br><br>

                1939 Le 18 octobre, dans le cadre du Comité Inter-Mouvements de jeunesse, les EU participent à la création de la CIMADE (Comité Inter-Mouvements Auprès Des Évacués) pour venir en aide, notamment, aux populations évacuées d’Alsace et de Lorraine<br><br>

                1930 Le 27 avril, les Éclaireurs Unionistes de France sont reconnus « association d’utilité publique »<br><br>

                1921 En juillet, création de la Fédération Française des Éclaireuses (F.F.E) regroupant des sections neutres et unionistes, rejoints en 1924 par les Éclaireuses israélites<br><br>

                1921 Le 14 janvier, les Éclaireurs Unionistes obtiennent la médaille de vermeil de la reconnaissance française pour services rendus à la nation pendant la Grande Guerre<br><br>

                1912 Apparition des premières sections d’éclaireuses, notamment au foyer de l’Union Chrétienne des Jeunes Filles (UCJF)<br><br>

                1911 Début 1911, création des deux premières troupes d’éclaireurs unionistes, au foyer de la mission populaire évangélique de Grenelle par le pasteur Gallienne, et à l’Union Chrétienne de Jeunes Gens (UCJG) de Boulogne par Adrien Alger<br><br>
